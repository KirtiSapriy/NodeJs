const mongoose = require('mongoose')

let schema = mongoose.Schema({
    fname: {
        type: String,
        required: true
    },
    lname: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    }
})

const Schema = mongoose.model('DemoAdmin', schema)

module.exports = Schema